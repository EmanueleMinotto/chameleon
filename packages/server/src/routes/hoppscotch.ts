// ── Handlers ───────────────────────────────────────────────────────────────────

export function handleExplorerPage(
  c: Context,
  routes: ChameleonRoute[],
  title: string,
  explorerPath: string,
): Response {
  const origin = new URL(c.req.url).origin;
  const collectionUrl = `${origin}${explorerPath}/collection.json`;
  const html = buildHtml(title, explorerPath, routes, collectionUrl);
  return c.html(html);
}

export function handleHoppscotchCollection(
  c: Context,
  routes: ChameleonRoute[],
  title: string,
  baseUrl: string,
): Response {
  const collection = buildHoppscotchCollection(routes, baseUrl, title);
  return c.json(collection);
}

export function handlePostmanCollection(
  c: Context,
  routes: ChameleonRoute[],
  title: string,
  baseUrl: string,
): Response {
  const collection = buildPostmanCollection(routes, baseUrl, title);
  return c.json(collection);
}
