import type { Context } from "hono";
import type { ChameleonRoute } from "@chameleon/core";
import { randomUUID } from "node:crypto";

// ── Hoppscotch collection ──────────────────────────────────────────────────────

function buildHoppscotchCollection(routes: ChameleonRoute[], baseUrl: string, title: string) {
  const restRoutes = routes.filter((r) => r.source !== "graphql");
  return {
    v: 1,
    name: title,
    folders: [],
    requests: restRoutes.map((route) => ({
      v: "1",
      id: randomUUID(),
      name: `${route.method} ${route.path}`,
      method: route.method,
      endpoint: `${baseUrl}${route.path}`,
      headers: [],
      params: [],
      body: { contentType: null, body: null },
      auth: { authType: "none", authActive: false },
      requestVariables: [],
    })),
  };
}

// ── Postman collection (v2.1) ──────────────────────────────────────────────────

function buildPostmanCollection(routes: ChameleonRoute[], baseUrl: string, title: string) {
  const restRoutes = routes.filter((r) => r.source !== "graphql");
  return {
    info: {
      name: title,
      _postman_id: randomUUID(),
      schema: "https://schema.getpostman.com/json/collection/v2.1.0/collection.json",
      description: "Generated by Chameleon",
    },
    item: restRoutes.map((route) => {
      const segments = route.path.split("/").filter(Boolean);
      const pathSegments = segments.map((s) => (s.startsWith("{") ? `:${s.slice(1, -1)}` : s));
      const pathVars = segments
        .filter((s) => s.startsWith("{"))
        .map((s) => ({ key: s.slice(1, -1), value: "" }));
      const url: Record<string, unknown> = {
        raw: `{{baseUrl}}${route.path}`,
        host: ["{{baseUrl}}"],
        path: pathSegments,
      };
      if (pathVars.length) url["variable"] = pathVars;
      return {
        name: `${route.method} ${route.path}`,
        request: { method: route.method, header: [], url },
        response: [],
      };
    }),
    variable: [{ key: "baseUrl", value: baseUrl, type: "string" }],
  };
}

// ── Landing page ───────────────────────────────────────────────────────────────

